#include <iostream>
#include <string>
using namespace std;
// this file is made to understand loop.


int main () {
    // this is for loop.
    for (int i = 1; i <= 10; i++) {
        cout << i << endl;
    }
    // this is while loop.
    int j=1;
    while (j<=5) {
        cout << j << endl;
        j++;
    }
    // this is do wile loop
    int k = 1;
    do {
        cout << k << endl;
        k++;
    }while (k<=8);
}